"""
This program converts a temperature from Celsius to Fahrenheit.
"""

celsius = float(input("Enter temperature in Celsius: "))
fahrenheit = (celsius * 9/5) + 32
print(f"{celsius} degrees Celsius is equal to {fahrenheit} degrees Fahrenheit.")

# print displays the output to the user in a better format.

"""
Changing the value of celsius will change the output value of fahrenheit accordingly.
"""